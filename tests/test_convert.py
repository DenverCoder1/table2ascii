from table2ascii import table2ascii as t2a


def test_normal():
    text = t2a(
        ["#", "G", "H", "R", "S"],
        [["1", "30", "40", "35", "30"], ["2", "30", "40", "35", "30"]],
        ["SUM", "130", "140", "135", "130"],
    )
    assert text == (
        "╔═════╦═══════════════════════╗\n"
        "║  #  ║  G     H     R     S  ║\n"
        "╟─────╫───────────────────────╢\n"
        "║  1  ║  30    40    35    30 ║\n"
        "║  2  ║  30    40    35    30 ║\n"
        "╟─────╫───────────────────────╢\n"
        "║ SUM ║ 130   140   135   130 ║\n"
        "╚═════╩═══════════════════════╝"
    )
